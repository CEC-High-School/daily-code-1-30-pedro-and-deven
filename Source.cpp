#include <iostream>

using namespace std;

int main() {


	for (int i = 20; i <= 80; ++i)
		std::cout << i << ' ';
	
	for (int i = 70; i >= 10; --i)
		std::cout << i << ' ';

	int i = 20;

	// while loop execution
	while (i <= 80) {
		cout << ' ' << i ;
		i++;

	}

	i = 70;

	// while loop execution
	while (i >= 10) {
		cout << ' ' << i;
		i--;

	}
	












}